#import "ZZRadioButton.h"

@implementation ZZRadioButton

- (CGRect)imageRectForContentRect:(CGRect)contentRect
{
    return CGRectMake(0, 0, self.frame.size.width * 0.4, self.frame.size.height);
}
- (CGRect)titleRectForContentRect:(CGRect)contentRect
{
    return CGRectMake(self.frame.size.width * 0.4, 0, self.frame.size.width * 0.6, self.frame.size.height);
}

@end
